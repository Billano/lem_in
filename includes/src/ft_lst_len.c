#include "libft.h"

size_t	ft_lst_len(t_lst *lst)
{
	size_t	i;
	t_lst	*tmp;

	i = 0;
	tmp = lst;
	while (tmp)
	{
		i++;
		tmp = tmp->next;
	}
	return (i);
}
